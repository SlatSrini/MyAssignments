package week1.day1;

public class PositiveorNot {

	public static void main(String[] args) {
		
		int n=35;
		
		if(n>0)
		{
			System.out.println(n+" is a Positive Number");
		}
		else if(n<0)
		{
			System.out.println(n+" is a Negative Number");
		}
		else
		{
			System.out.println(n+" is Neither Positive nor Negative");
		}

	}

}
